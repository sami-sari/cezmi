package com.samisari.cezmi.core;

import java.awt.event.ComponentAdapter;
import java.awt.event.ComponentEvent;

import org.apache.log4j.Logger;

public class Java2DPanelComponentListener extends ComponentAdapter {
	private static final Logger logger = Logger.getLogger(Java2DPanelComponentListener.class);

	@Override
	public void componentResized(ComponentEvent e) {
		logger.debug(e.getComponent().getWidth()  +","+e.getComponent().getHeight());
		e.getComponent().getSize();
		
	}
}
