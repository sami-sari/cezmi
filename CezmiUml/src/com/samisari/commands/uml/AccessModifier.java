package com.samisari.commands.uml;

public enum AccessModifier {
	PUBLIC("public"), PRIVATE("private"), PROTECTED("protected"), ABSTRACT("abstract"), STATIC("static"), FINAL("final"), SYNCHRONIZED(
			"synchronized"), TRANSIENT("transient"), VOLATILE("volatile");
	private String name;

	private AccessModifier(String name) {
	}

	public String getName() {
		return name;
	}
}
