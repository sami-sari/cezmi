package com.samisari.cezmi.animator.animationcommands;

import com.samisari.cezmi.core.AbstractCommand;

public abstract class CezmiAbstractTransformation {
	// Object to which transformation will be applied
	private AbstractCommand object;
	// Start frame of the transformation
	private int startFrame;
	// End frame of the transformation
	private int endFrame;
	
	public abstract void transform();

	public AbstractCommand getObject() {
		return object;
	}

	public void setObject(AbstractCommand object) {
		this.object = object;
	}


	public int getStartFrame() {
		return startFrame;
	}

	public void setStartFrame(int startFrame) {
		this.startFrame = startFrame;
	}

	public int getEndFrame() {
		return endFrame;
	}

	public void setEndFrame(int endFrame) {
		this.endFrame = endFrame;
	}
}
