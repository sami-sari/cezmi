package com.samisari.graphics.commands;

import org.apache.log4j.Logger;

import com.samisari.cezmi.component.CmdRectangle;

public class CmdJTextBox extends CmdRectangle implements Cloneable {
	private static final long serialVersionUID = 5226672462820897805L;
	public static final Logger logger = Logger.getLogger(CmdJTextBox.class);
	private String name = "TextField";
	private String mask = "";
	private int maxLength = 20;
	private String changeEventHandler;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getMask() {
		return mask;
	}

	public void setMask(String mask) {
		this.mask = mask;
	}

	public int getMaxLength() {
		return maxLength;
	}

	public void setMaxLength(int maxLength) {
		this.maxLength = maxLength;
	}

	public String getChangeEventHandler() {
		return changeEventHandler;
	}

	public void setChangeEventHandler(String changeEventHandler) {
		this.changeEventHandler = changeEventHandler;
	}

}
